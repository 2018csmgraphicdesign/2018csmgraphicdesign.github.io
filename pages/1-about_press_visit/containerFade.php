        <section class="column">
          <p class="body-text">As the public's relationship with the media continuously shifts, the role of designers and the  individual decisions they make is under increased scrutiny. Honesty has become a key issue of the contemporary media landscape, which is why we wanted to reveal the honest intentions within the work through a transparent and functional system.
          <br>&nbsp; &nbsp; The title of this year’s BA Graphic Design degree show, Sincerely, (with a comma) emerged from conversations that shaped the last year of the current graphic design program. The title is ambiguous: to align the collaborations and projects in the show with a shared sense that graphic design needs more transparency and honesty as a practice.
        </section>
        <section class="column">
          <p class="body-text">But at the same time to connote the language of email and an “aesthetic of administration” that has inspired the names of the rooms and the visual identity of the show. As we increasingly struggle to distinguish between filtered data and credible evidence, real and fake news, a nod to algorithmic and bureaucratic processes also seems pertinent: Is it actually possible to be sincere when all around us sincerity is being performed and evidence is dismissed as opinion?
          <br>&nbsp; &nbsp; Coined by critic Benjamin Buchloh in 1999 to describe Conceptual Art of the 1960s, the phrase “aesthetic of administration” described an art with self-imposed restrictions and a lack of totalizing vision, by practitioners like Sol LeWitt.</p>
        </section>
        <section class="column">
          <p class="body-text">The phrase also alluded to what critics of the culture industry argued was the inescapable logic and self-referencing value system of the “fully administered world” (Adorno, 1997): when everything is tracked, measured, monetized and commodified, we are left to contemplate the role of the graphic designer in either perpetuating this administration—or in drawing attention to it. In 1967, Guy Debord wrote of the spectacle that “covers the entire surface of the world and bathes endlessly in its own glory” (Debord, 1967).
          <br>&nbsp; &nbsp; In 2018, the conditions, constraints and codes that generate works of graphic design become a prominent feature of the curatorial system.</p>
        </section>
